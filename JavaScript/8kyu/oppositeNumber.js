/*
Task:
-----
Very simple, given a number, find its opposite.

Examples:

1: -1
14: -14
-34: 34
*/

function opposite (number) {
  // The - operator also have unary version, where they operate only on one variable
  // - returns its negative counterpart
  return -number
}

// TEST CASES
console.log(opposite(55)) // -55
